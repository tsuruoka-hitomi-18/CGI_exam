const season = "夏" // 自由に値を変更してみましょう。
switch (season) {
  case "春": // seasonが春の場合
    console.log("桜を見にいきます");
    break;
  case "夏":　// seasonが夏の場合
    console.log("アイスを食べます");
    break;
  case "秋": // seasonが秋の場合
    console.log("食欲の秋です");
    break;
  case "冬": // seasonが冬の場合
    console.log("雪が降ります");
    break;
  default:
    console.log("音楽を聞きます");
}
